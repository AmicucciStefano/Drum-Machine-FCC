function App() {

  const [volume, setVolume] = useState(1);
  const [estado, setStado] = useState(true);

  const onOff = () => {
    setStado(!estado)
  }

  return (
    <div className="min-vh-100 text-white">
      <div className='text-center'>
      <h2>Drum Machine</h2>
      <div className='container-pad d-flex w-75 ms-auto me-auto'>
        {estado ?
         <div className='row-cols-md-4 w-50'>
            {audioClips.map(clip => (<Pad key={clip.id} clip={clip} volume={volume} />))}
          </div>  
          :
          <div className='row-cols-md-4 w-50'>
            {otherAudioClips.map(clip => (<Pad key={clip.id} clip={clip} volume={volume} />))}
          </div>
        }
        <div className='w-50'>
        <div className='mt-5 me-2'>
          <span className='text-black  fw-bold'>Bank</span>
        </div>
          <div onClick={onOff} className='switch'>
            <div className={estado ? "right" : "left"}></div>
          </div>
          <h4 className='text-black'>Volume</h4>
          <input type="range" step="0.01" onChange={(e) => setVolume(e.target.value)} value={volume} max="1" min="0" className="w-50" /></div>
        </div>
      </div>
    </div>
  );
}

export default App;
